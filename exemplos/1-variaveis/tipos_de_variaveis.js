/**tres formas de criar variavis
 * var  -> não reipeita o scopo de bloco
 * let  -> respeita os escopos
 * const  -> cria constante respeita bloco e permite que troque de propriedade em objeto
 */
var nameVar ='João';
let nameLet ='Thereza';
const nameConst ='Luciana';


console.log('nameVar:'+ nameVar);
console.log('nameLet:'+ nameLet);
console.log('nameConst:'+ nameConst); 